CREATE DATABASE trailerflix;
USE trailerflix;

CREATE TABLE IF NOT EXISTS Categorias (
	idCategoria int auto_increment NOT NULL UNIQUE,
	nombreCategoria nvarchar(50) NOT NULL UNIQUE,
	PRIMARY KEY (idCategoria)
);

CREATE TABLE IF NOT EXISTS Generos (
	 idGenero  int  auto_increment  NOT NULL UNIQUE,
	 Nombre  nvarchar(50) NOT NULL UNIQUE,
	PRIMARY KEY ( idGenero )
);

CREATE TABLE IF NOT EXISTS Actores  (
	 id  int  auto_increment  NOT NULL UNIQUE,
	 nombreCompleto  nvarchar(200) NOT NULL,
	PRIMARY KEY ( id )
);

CREATE TABLE IF NOT EXISTS Catalogo  (
	 id  int  auto_increment  NOT NULL UNIQUE,
	 titulo  nvarchar(255) NOT NULL,
	 poster  nvarchar(255),
	 idCategoria  int NOT NULL,
	 idGenero  int NOT NULL,
	 resumen  nvarchar(250) NOT NULL,
	 temporadas  nvarchar(50),
	 duracion  nvarchar(50),
	 trailer  nvarchar(200),
	PRIMARY KEY ( id )
);

CREATE TABLE IF NOT EXISTS Tags  (
	 idTag  int  auto_increment  NOT NULL UNIQUE,
	 nombre  nvarchar(50) NOT NULL UNIQUE,
	PRIMARY KEY ( idTag )
);

CREATE TABLE IF NOT EXISTS Reparto  (
	 idCatalogo  int nOT NULL,
	 idActores  int NOT NULL
);

CREATE TABLE IF NOT EXISTS TagsCatalogo  (
	 idCatalogo  int NOT NULL,
	 idTags  int NOT NULL
);




